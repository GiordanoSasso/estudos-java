package EstruturasDeControle;

public class For1 {
    public static void main(String[] args) {
        for(int contador = 0; contador <= 20; contador += 2) {
            System.out.printf("i = %d\n", contador);
        }
    }
}
/*for é usado quando se tem um estrutura
 * já pré-determinada com as repetições
 * já definidas
 * 
 * primeira parte declarar a variável
 * 
 * segunda parte a expressão que vai retornar TRUE ou FALSE
 * 
 * terceira parte será a mudança ou incremento
 */